// HARD: Create 4 math functions, one called add() that adds 2 numbers, one called subtract() that subtracts 2 numbers, one
// called multiply() to multiply 2 numbers, and one called divide() to divide two numbers.


//Addition
function add(number1, number2) {
    return number1 + number2;

}

//Subtraction
function subtract(number1, number2) {
    return number1 - number2;
}

//Multiplication
function multiply(number1, number2) {
    return number1 * number2;
}

//Division
function divide(number1, number2) {
    return number1 / number2;
}

console.log(add(99, 40), subtract(10, 7), multiply(9, 9), divide(50, 5));

